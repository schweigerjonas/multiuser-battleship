package fenster;

/**
 *
 * @author Fiedler
 */
public class Punkt {
    private int x, y;

    public Punkt(int x, int y){
        this.x = x;
        this.y = y;
    }

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @return the y
     */
    public int getY() {
        return y;
    }

}
